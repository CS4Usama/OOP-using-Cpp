//Write a program that displays the sizes of different data types.

#include <iostream>
using namespace std;
int main()
{
	cout<<"Size of 'char' Data Type in Bytes: "<<sizeof(char)<<endl;
	cout<<"Size of 'int' Data Type in Bytes: "<<sizeof(int)<<endl;
	cout<<"Size of 'float' Data Type in Bytes: "<<sizeof(float)<<endl;
	cout<<"Size of 'long' Data Type in Bytes: "<<sizeof(long)<<endl;
	cout<<"Size of 'double' Data Type in Bytes: "<<sizeof(double)<<endl;
	cout<<"Size of 'long double' Data Type in Bytes: "<<sizeof(long double)<<endl;
	return 0;
}
